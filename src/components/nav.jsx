import { useState, useEffect } from "react";
import { useSearch } from "../context/search";
import { Link, useNavigate } from "react-router-dom";
import { useCategories } from "../context/notifications";
import { useMediaQuery } from "react-responsive";
import MMobile from "./menu_mobile";
import { useCart } from "../context/cart";
import { IoBagHandleOutline } from "react-icons/io5";
import { CiSearch } from "react-icons/ci";
import TopInfo from "./top";

const Navbar = () => {
    const [isOpen, setIsOpen] = useState(false);
    const [isMenuOpen, setIsMenuOpen] = useState(false);
    const isMobile = useMediaQuery({ maxWidth: 640 }); // Detectar dispositivos móviles
    const navigate = useNavigate();
    const { setOpenSearch } = useSearch();
    const { products } = useCategories();
    const { cartItems } = useCart();
    const [cartCount, setCartCount] = useState(cartItems.length); // Puedes conectar esto con el estado real del carrito

    const toggleDropdown = () => {
        setIsOpen(!isOpen);
    };

    const toggleMenu = () => {
        setIsMenuOpen(!isMenuOpen);
    };

    // Manejador para abrir el menú cuando el ratón entra
    const handleMouseEnter = () => {
        setIsOpen(true);
    };

    // Manejador para cerrar el menú cuando el ratón sale
    const handleMouseLeave = () => {
        setIsOpen(false);
    };

    useEffect(() => {
        const handleClickOutside = (event) => {
            // Verifica si el clic ocurrió fuera del menú móvil
            if (
                !event.target.closest(".menu-mobile") && // Clase asociada al contenedor del menú móvil
                !event.target.closest(".menu-button")    // Botón del menú
            ) {
                setIsMenuOpen(false);
            }
        };

        if (isMenuOpen) {
            document.addEventListener("click", handleClickOutside);
        }

        return () => {
            document.removeEventListener("click", handleClickOutside);
        };
    }, [isMenuOpen]);

    return (
        <div className="w-full z-999">
            <TopInfo />
            <nav className="bg-white border border-b">
                <div className="container px-4 mx-auto justify-center ">
                    <div className="relative flex items-center justify-between h-16">
                        {/* Logo */}
                        <div className="flex-shrink-0">
                            <Link to="/" className="text-black md:text-xl text-sm font-bold no-underline font-poppins">
                                Veterinaria La Comercial
                            </Link>
                        </div>

                        {/* Menú principal en pantallas grandes */}
                        <div className="flex-1 flex items-center justify-center sm:items-stretch sm:justify-start">
                            <div className="hidden sm:block sm:ml-6">
                                <div className="flex space-x-4">
                                    <Link
                                        to="/servicios"
                                        className="text-black hover:text-gray-900 px-3 py-2 rounded-md text-sm font-medium no-underline font-poppins"
                                    >
                                        SERVICIOS
                                    </Link>
                                    <Link
                                        to="/blog"
                                        className="text-black hover:text-gray-900 px-3 py-2 rounded-md text-sm font-medium no-underline font-poppins"
                                    >
                                        BLOG
                                    </Link>

                                    {/* Dropdown de Categorías */}
                                    <div
                                        className="relative"
                                        onMouseEnter={handleMouseEnter}  // Abre el menú cuando el ratón entra
                                        onMouseLeave={handleMouseLeave}  // Cierra el menú cuando el ratón sale
                                    >
                                        <button
                                            onClick={toggleDropdown}
                                            className="text-black px-3 py-2 rounded-md text-sm font-medium flex items-center font-poppins"
                                        >
                                            EXPLORAR CATEGORÍAS
                                            <svg
                                                className="-mr-1 ml-2 h-5 w-5"
                                                xmlns="http://www.w3.org/2000/svg"
                                                fill="none"
                                                viewBox="0 0 24 24"
                                                stroke="currentColor"
                                                aria-hidden="true"
                                            >
                                                <path
                                                    strokeLinecap="round"
                                                    strokeLinejoin="round"
                                                    strokeWidth="2"
                                                    d="M19 9l-7 7-7-7"
                                                />
                                            </svg>
                                        </button>

                                        {/* Menú desplegable */}
                                        {isOpen && (
                                            <div
                                                className={`absolute bg-white shadow-lg rounded-lg left-1/2 transform -translate-x-1/2  z-50 w-full sm:w-[1000px] md:w-${products.length} lg:w-${products.length}`}
                                            >
                                                <div className={`grid grid-cols-1 sm:grid-cols-2 md:grid-cols-3 lg:grid-cols-${products.length}`}>
                                                    {products.length > 0 ? products
                                                        .sort((a, b) => a.productoTipo.localeCompare(b.productoTipo))
                                                        .map((category, i) => (
                                                            <div key={i} className="w-full">
                                                                <div className="w-full bg-red-600 px-4 py-1">
                                                                    <h3 className="text-lg text-white text-left font-poppins">
                                                                        {category.productoTipo.toUpperCase()}
                                                                    </h3>
                                                                </div>
                                                                <div className=" text-left">
                                                                    {category.categorias
                                                                        .sort((a, b) => a.localeCompare(b)) // Ordenar categorías alfabéticamente
                                                                        .map((product, index) => (
                                                                            <div key={index} className="my-1 pl-4">
                                                                                <Link
                                                                                    to={`/shop/${category.productoTipo}/${product}`}
                                                                                    className="text-gray-600 hover:text-gray-700 no-underline font-questrial relative group"
                                                                                >
                                                                                    {product.toUpperCase()}
                                                                                    <span className="absolute left-0 bottom-[-5px] h-[2px] w-0 bg-red-500 transition-all duration-300 ease-in-out group-hover:w-full"></span>
                                                                                </Link>
                                                                            </div>
                                                                        ))}
                                                                </div>
                                                            </div>
                                                        )) : <p>Cargando...</p>}
                                                </div>
                                            </div>
                                        )}

                                    </div>
                                </div>
                            </div>
                        </div>

                        {/* Íconos a la derecha */}
                        <div className="flex items-center space-x-4">
                            <button
                                className="text-black focus:outline-none"
                                onClick={() => setOpenSearch(true)}
                            >
                                <CiSearch className="text-2xl" />
                            </button>

                            <div className="relative">
                                <button
                                    className="text-black focus:outline-none"
                                    onClick={() => navigate("/cart")}
                                >
                                    <IoBagHandleOutline className="text-2xl" />
                                </button>
                                {cartCount > 0 && (
                                    <span className="absolute top-0 right-0 inline-flex items-center justify-center px-2 py-1 text-xs font-bold leading-none text-red-100 bg-red-600 rounded-full transform translate-x-1/2 -translate-y-1/2">
                                        {cartCount}
                                    </span>
                                )}
                            </div>

                            {/* Menú móvil */}
                            {
                                isMobile && (
                                    <button
                                        onClick={toggleMenu}
                                        className="text-black focus:outline-none menu-button"
                                    >
                                        <svg
                                            xmlns="http://www.w3.org/2000/svg"
                                            width="24"
                                            height="24"
                                            viewBox="0 0 24 24"
                                            fill="none"
                                            stroke="currentColor"
                                            strokeWidth="2"
                                            strokeLinecap="round"
                                            strokeLinejoin="round"
                                            className="icon icon-tabler icon-tabler-menu-2"
                                        >
                                            <path stroke="none" d="M0 0h24v24H0z" fill="none" />
                                            <path d="M4 6h16" />
                                            <path d="M4 12h16" />
                                            <path d="M4 18h16" />
                                        </svg>
                                    </button>
                                )
                            }
                        </div>
                    </div>

                    {/* Menú móvil desplegable */}
                    {isMenuOpen && (
                        <MMobile setIsMenuOpen={setIsMenuOpen} products={products} toggleDropdown={toggleDropdown} />
                    )}
                </div>
            </nav>
        </div>
    );
};

export default Navbar;
